package dto.recommended;

public class FertilizationRecommendationDto {
	
	private Double n;
	private Double p;
	private Double k;
	private Double mg;
	private Double b;
	private Double ca;
	public Double getN() {
		return n;
	}
	public void setN(Double n) {
		this.n = n;
	}
	public Double getP() {
		return p;
	}
	public void setP(Double p) {
		this.p = p;
	}
	public Double getK() {
		return k;
	}
	public void setK(Double k) {
		this.k = k;
	}
	public Double getMg() {
		return mg;
	}
	public void setMg(Double mg) {
		this.mg = mg;
	}
	public Double getB() {
		return b;
	}
	public void setB(Double b) {
		this.b = b;
	}
	public Double getCa() {
		return ca;
	}
	public void setCa(Double ca) {
		this.ca = ca;
	}
	
	

}
